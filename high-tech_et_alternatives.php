            <div class="right">
                <div>
                    <p>
                        Les <span>nouvelles technologies</span> sont une réelle <span>addictions</span> pour certaine personne, la constance stimulation de
                        notre cerveau par des podcasts audio, de la musique, des séries entraine une réelle dépendance et la
                        moindre absence de distraction en font paniquer plus d’un. Pour ne pas lier a ce phénomène, un <span>retour aux
                        activités traditionnelles</span> de divertissement peut être une première solution.
                    </p>
                </div>
