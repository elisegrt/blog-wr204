                ordinateurs portables, objets connectés, jeux vidéo et réseaux sociaux sont devenus des incontournables
                de notre vie sociale, professionnelle et personnelle. Cependant, l'utilisation excessive de ces
                technologies a des <span>effets néfastes</span> sur notre <span>santé mentale et physique</span>, notre <span>environnement</span> et notre
                <span>relation aux autres</span>. De plus en plus de personnes cherchent des <span>alternatives</span> au numérique high-tech pour
                <span>réduire leur dépendance</span> à ces technologies. Dans cet article, nous allons explorer quelques-unes des
                alternatives possibles.
            </p>

