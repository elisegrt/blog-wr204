                        un <span>journal</span>, une <span>lettre</span>, regarder des <span>photos</span>, toutes ces activités sont réalisable sans la moindre
                        utilisation d’appareil connecté, cela peut être une première étape dans la réduction de technologies.
                    </p>
                    <p>
