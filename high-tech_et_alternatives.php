            <h4><span>2</span> Le minimalisme numérique</h4>
            <div class="right">
                <div>
                    <p>
                        Le <span>minimalisme numérique</span> est une tendance qui consiste à <span>réduire son utilisation des technologies</span>. Parce que
                        de nos jours il est évident que supprimer toutes technologies de son quotidien est presque infaisable,
                        il est tout a fait possible de brimer sa relation avec celle-ci. Cela peut passer par la <span>désactivation des
                        notifications inutiles</span> pour n’être prévenu que lorsqu’une information ou un message essentiel nous parvient.
                        Supprimer certaines applications est aussi un moyen effiace : certes, il sera toujours possible de les
                        utiliser via un navigateur internet mais c’est tout l’objectif ! Rajouter des étapes pour faire quelque chose devient
                        vite pénible et cela encourage à en réduire la consommation.
                    </p>
                    <p>
                        Une méthode plus radicale serait de <span>revenir sur un model d’ordinateur ou de smartphone plus ancien</span>, pour
                        être certain ne pas être tenté d’aller sur tel ou tel réseau social ou quelconque application. Le terme
                        <span>“digital detox”</span> est de plus en plus employé par certaine personnes qui font le choix de totalement couper
                        le liens avec la technologie pendant une période donnée, c’est l’ultime solution, difficiile à mettre en
                        application mais efficace.
                    </p>
                </div>
