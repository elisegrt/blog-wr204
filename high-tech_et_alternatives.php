            <h4><span>3</span> Les technologies alternatives</h4>
            <div class="left">
                <img class="img_right" src="images/environnement.webp" alt="">
                <div>
                    <p>
                        En plus d'être une un besoin personnel, <span>limiter son utilisation de la technologie</span> est également 
                        devenu une <span>exigence environnementale</span>. Nous avons vu en amont de cet article, que la technologie est
                        un outil indispensable mais polluant. Bien que la majorité de la pollution soit produite par les grandes
                        firmes internationales, il est tout a fait possible de <span>jouer un rôle individuel</span> dans cette <span>transition
                        écologique</span>.
                    </p>
                    <p>
                        Afin de réduire, à la fois son impact environnement et son utilisation des objets high-tech, il est
                        envisageable de <span>revenir aux sources</span>, d’utiliser des <span>technologies alternatives</span>. Montres analogiques,
                        appareils photos argentiques, ordinateurs à faible consommation énergétique ou encore l’utilisation
                        d’éclairage LED sont des <span>petits changements de notre quotidien</span> qui, à grande échelle, peuvent avoir un
                        <span>réel impact sur notre futur</span>.
                    </p>
                </div>
            </div>

